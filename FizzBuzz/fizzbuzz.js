
// Loop 1 to 10
for(let i = 1; i<11;i++){
 if(i % 2 == 1){
   console.log("fizz");
 }
 else {
   console.log(i);
 }
  // odd number print fizz
};

// loop 10 to 1
for(let i = 10; i>0;i--){
  if(i % 2 == 1){
    console.log("buzz");
  }
  else {
    console.log(i);
  }
  // even number print buzz
};